    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function createQ(Request $request)
    {
        $request->validate([
            'pitanje'=>'required',
            'odgovor'=>'required',
            'asocijacija'=>'required',
            'exam_id'=>'required',
         ]);

        $qu= new Question();
        $qu->pitanje=$request->pitanje;
        $qu->odgovor=$request->odgovor;
        $qu->asocijacija=$request->asocijacija;
        $qu->exam_id=$request->exam_id;
        $res=$qu->save();
        if($res==true){
           return redirect('/testpage')->with('success','Uspe≈°no ste se kreirali pitanje!');
        }
    }

